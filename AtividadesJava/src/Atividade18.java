public class Atividade18 {
    public static void main(String[] args) {
        int p = 6;
        int q = 1;
        if (p > q) {
            System.out.println("P é maior que Q.");
        }
        if (p < q) {
            System.out.println("P é menor que Q");
        }
        if (p == q) {
            System.out.println("P é igual a Q");
        }
    }
}
